class Actions {
    Integer addition(int a, int b){
        int c = a + b;
        return c;
    }
    Integer subtraction(int a, int b){
        int c = a - b;
        return c;
    }
    Integer multiplication(int a, int b){
        int c = a * b;
        return c;
    }
    Integer division(int a, int b){
        int c = a / b;
        return c;
    }
}
